
//used to store the coordinates of each individual 
public class Coordinate {

	private int row;	//stores index of the row
	private int col;	//stores index of the column
	
	public Coordinate(int row, int col) {
		this.row = row;
		this.col = col;
	}

	public int getRow() {
		return row;
	}

	public void setRow(int row) {
		this.row = row;
	}

	public int getCol() {
		return col;
	}

	public void setCol(int col) {
		this.col = col;
	}
	
}


